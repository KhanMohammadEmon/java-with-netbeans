
package javalecture3;

public class SuperMan {
    
    
    public String name,power;
    
    public SuperMan(String name , String power)
    {
        this.name = name;
        this.power = power;
    }
    
    public void display()
    {
        System.out.println("Name : "+name+"; Power : "+power);
    }

   
    
}
